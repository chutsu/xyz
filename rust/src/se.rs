use camera::Resolution;


///////////////////////////////////////////////////////////////////////////////
// CAMERA GEOMETRY                                                           //
///////////////////////////////////////////////////////////////////////////////

#[derive(Debug)]
pub struct CameraGeometry {
  pub camera_id: u8,
  pub camera_model: String,
  pub resolution: Resolution,
  pub intrinsic: Vec<f64>,
  pub extrinsic: Vec<f64>,
}

///////////////////////////////////////////////////////////////////////////////
// IMU GEOMETRY                                                              //
///////////////////////////////////////////////////////////////////////////////

#[derive(Debug)]
pub struct ImuGeometry {
  pub imu_id: u8,
  pub extrinsic: Vec<f64>,
}
